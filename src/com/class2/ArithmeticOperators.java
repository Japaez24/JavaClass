package com.class2;

public class ArithmeticOperators {

	public static void main(String[] args) {

		// ArithmeticOperators

		// Declare two int. variables and than we perform:
		// Addition, Subtraction, Multiplication, Division

		int a = 30;
		int b = 15;

		System.out.println(a + b); // The + in the paranthesis plays a different role now(addition)

		System.out.println(a - b); // the -

		System.out.println(a * b); // the *

		System.out.println(a / b); // the /

		// The preferred way to do this is as follows

		int sum = a + b;
		int sub = a - b;
		int mult = a * b;
		int div = a / b;

		System.out.println(sum);
		System.out.println(sub);
		System.out.println(mult);
		System.out.println(div);

		// Task
		// Print: the sum __ two numbers is __

		System.out.println("the addition of two numbers is " + sum + ".");
		System.out.println("the Subtraction of two numbers is " + sub + ".");
		System.out.println("the multiplication of two numbers is " + mult + ".");
		System.out.println("the division of two numbers is " + div + ".");

		
		
		
	
		
		
		
	}

}
